class EqualsAndDifferent {

public int compare;

    public int compare(int firstNumber, int secondNumber) {
        if (firstNumber == secondNumber) {
            System.out.println("Numbers are equals");
        } else {
            System.out.println("Numbers are different");

        }
        return compare;
    }

}
