class DefaultConstructorV3 {

    private String fullName;
    private int age;
    private boolean male;
    private Boolean female;

    public String getFullName() {
        return this.fullName;
    }

    public int getAge() {
        return this.age;
    }

    public boolean getMale() {
        return male;
    }

    public Boolean getFemale() {
        return female;
    }

}