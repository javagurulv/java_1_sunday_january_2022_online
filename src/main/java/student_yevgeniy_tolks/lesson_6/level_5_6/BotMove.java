class BotMove {
    private int xBot;
    private int yBot;

    public BotMove(int xBot, int yBot) {
        this.xBot = xBot;
        this.yBot = yBot;
    }

    public int getXBot() {
        return xBot;
    }

    public int getYBot() {
        return yBot;
    }
}
