package student_vitaly_galuzo;

public class Human {

    String name;
    int age;
    int weight;




    public Human(String name,int age,int weight){
        this.name = name;
        this.age = age;
        this.weight = weight;

    }

    public String getName() {
        return this.name;

    }

    public int getAge(){
        return this.age;
    }

    public int getWeight() {
        return this.weight;
    }
}
