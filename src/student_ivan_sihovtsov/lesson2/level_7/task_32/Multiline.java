import java.util.Scanner;

public class Multiline {

    public static void main(String[] args) {

        Scanner scanner = new Scanner(System.in);

        System.out.println("Write a comment: ");
        String comment = scanner.nextLine();
        String comment1 = scanner.nextLine();
        String comment2 = scanner.nextLine();
        String comment3 = scanner.nextLine();
        String comment4 = scanner.nextLine();
        String comment5 = scanner.nextLine();

        System.out.println("Your comment:");

        System.out.println(comment);
        System.out.println(comment1);
        System.out.println(comment2);
        System.out.println(comment3);
        System.out.println(comment4);
        System.out.println(comment5);





    }

}
