package student_devids_kropacovs.lesson8.level1;


class Trader {

    private String fullName;
    private String city;

    Trader (String fullName, String city){
        this.fullName = fullName;
        this.city = city;
    }

    public String getFullName(){
        return this.fullName;
    }

    public String getCity(){
        return this.city;
    }

}
