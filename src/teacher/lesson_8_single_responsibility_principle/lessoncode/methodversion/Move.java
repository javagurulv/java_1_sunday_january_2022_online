package teacher.lesson_8_single_responsibility_principle.lessoncode.methodversion;

class Move {

    private int x;
    private int y;

    Move(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }
}


